    public function data()
    {
        
        $driver = Driver::all();
        return DataTables()
            ->of($driver)
            ->addIndexColumn()
            ->addColumn('status', function ($driver) {
                if($driver->status == 1){
                    return '<span>Tersedia</span>';
                }else{
                    return '<span>Tidak Tersedia</span>';
                }
            })
            ->rawColumns(['status'])
            ->make(true);
    }

